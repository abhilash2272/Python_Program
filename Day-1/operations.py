def sum(a,b):
    c=a+b
    return c
def sub(a,b):
    c=a-b
    return c
def mul(a,b):
    c=a*b
    return c
def div(a,b):
    return a/b
def mod(a,b):
    return a%b
print(f"sum is{sum(10,20)}")
print(f"Difference is{sub(100,20)}")
print(f"Multiplication is{mul(10,20)}")
print(f"division is{div(100,20)}")
print(f"modulo is{mod(10,20)}")
